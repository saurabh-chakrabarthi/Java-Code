package com.java.poc.dsa.math;

public class IncrementOperations {
    public static void main(String[] args) {
        int i = 1;
        int j = 1;
        System.out.println("i : " + i + ", j : " + j);
        System.out.println("i++ : " + i++ + ", j : " + j++);
        System.out.println("i : " + i + ", j : " + j);
        System.out.println("++i : " + ++i + ", ++j : " + ++j);
    }
}
